package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("rengginang");
                break;
            case 2:
                System.out.println("ucing");
                break;
            case 3:
                System.out.println("energen");
                break;
            case 4:
                System.out.println("kulkas 8 pintu");
                break;
            default:
                System.out.println("Anda Ganteng");
                break;


        }
    }
}
